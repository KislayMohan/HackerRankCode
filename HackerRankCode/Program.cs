        public static int MaxEnvelopes(int[][] envelopes)
        {
            if (envelopes == null || envelopes.Length == 0) return 0;

            // Step 1: Sort the envelopes
            Array.Sort(envelopes, (a, b) => {
                if (a[0] == b[0]) return b[1] - a[1]; // Sort by height in descending order if widths are equal
                return a[0] - b[0]; // Otherwise, sort by width in ascending order
            });

            // Step 2: Find LIS (Longest Increasing Subsequence) based on height
            List<int> lis = new List<int>();

            foreach (var envelope in envelopes)
            {
                int height = envelope[1];
                int index = BinarySearch(lis, height);

                if (index == lis.Count)
                {
                    lis.Add(height); // Extend LIS
                }
                else
                {
                    lis[index] = height; // Replace element to maintain LIS property
                }
            }

            return lis.Count();
        }

        private static int BinarySearch(List<int> lis, int target)
        {
            int left = 0, right = lis.Count - 1;
            while (left <= right)
            {
                int mid = left + (right - left) / 2;
                if (lis[mid] < target)
                {
                    left = mid + 1;
                }
                else
                {
                    right = mid - 1;
                }
            }
            return left;
        }

        public static string RankTeams(string[] votes)
        {
            if (votes == null || votes.Length == 0)
                return string.Empty;

            int teamCount = votes[0].Length;
            Dictionary<char, int[]> rankMap = new Dictionary<char, int[]>();

            // Initialize ranking dictionary
            foreach (char team in votes[0])
            {
                rankMap[team] = new int[teamCount];
            }
            // Count the rankings for each team
            foreach (string vote in votes)
            {
                for (int i = 0; i < vote.Length; i++)
                {
                    rankMap[vote[i]][i]++;
                }
            }

            // Sort teams based on ranking rules
            var sortedTeams = rankMap.Keys.ToList();
            sortedTeams.Sort((a, b) => {
                for (int i = 0; i < teamCount; i++)
                {
                    if (rankMap[a][i] != rankMap[b][i])
                        return rankMap[b][i] - rankMap[a][i]; // Higher votes first
                }
                return a.CompareTo(b); // Alphabetical order if tie persists
            });

            return new string(sortedTeams.ToArray());
        }

        public static int[] BuildPrefixSum(string s, char target)
        {
            int n = s.Length;
            int[] prefixSum = new int[n];
            prefixSum[0] = (s[0] == target) ? 1 : 0;

            for (int i = 1; i < n; i++)
            {
                prefixSum[i] = prefixSum[i - 1] + ((s[i] == target) ? 1 : 0);
            }

            return prefixSum;
        }

        public static int[] SameEndSubstringCount(string s, int[][] queries)
        {
            int[] res = new int[queries.Count()];
            for (int i = 0; i < queries.Count(); i++)
            {
                var charCountDict = new Dictionary<char, int>();
                for (int j = queries[i][0]; j <= queries[i][1]; j++)
                {
                    if (charCountDict.ContainsKey(s[j]))
                    {
                        charCountDict[s[j]]++;
                    }
                    else
                    {
                        charCountDict[s[j]] = 1;
                    }
                }
                var count = queries[i][1] - queries[i][0] + 1;
                foreach (var item in charCountDict)
                {
                    if (item.Value > 1)
                    {
                        count += (item.Value * (item.Value - 1)) / 2;
                    }
                }
                res[i] = count;
            }

            return res;
        }

        public static int LongestConsecutive(int[] nums)
        {
            HashSet<int> num_set = new HashSet<int>(nums);
            int longestStreak = 0;
            foreach (int num in num_set)
            {
                if (!num_set.Contains(num + 1))
                {
                    int currentNum = num;
                    int currentStreak = 1;
                    while (num_set.Contains(currentNum - 1))
                    {
                        currentNum -= 1;
                        currentStreak += 1;
                    }

                    longestStreak = Math.Max(longestStreak, currentStreak);
                }
            }

            return longestStreak;
        }

        public static int WaysToPartition(int[] nums, int k)
        {
            var leftSum = new int[nums.Length];
            var rightSum = new int[nums.Length];

            for (int i = 0; i < nums.Length; i++)
            {
                if (i == 0)
                {
                    leftSum[i] = nums[i];
                    rightSum[nums.Length - i - 1] = nums[nums.Length - i - 1];
                }
                else
                {
                    leftSum[i] = leftSum[i - 1] + nums[i];
                    rightSum[nums.Length - i - 1] = nums[nums.Length - i - 1] + rightSum[nums.Length - i];
                }
            }
            int ways = 0;
            return ways;
        }

        public static int MyAtoi(string s)
        {
            var result = 0;
            var negative = false;
            int i = 0;
            while (i < s.Length && s[i] == ' ')
            {
                i++;
            }

            if (i < s.Length && s[i] == '+')
            {
                negative = false;
                i++;
            }
            else if (i < s.Length && s[i] == '-')
            {
                negative = true;
                i++;
            }

            while (i < s.Length && char.IsDigit(s[i]))
            {
                if (result > int.MaxValue / 10 ||
                    (result == int.MaxValue / 10 &&
                     s[i] - '0' > int.MaxValue % 10))
                {
                    return negative ? int.MinValue : int.MaxValue;
                }

                result = result * 10 + (s[i++] - '0');
            }

            return negative ? result * -1 : result;
        }

        public static string LongestCommonPrefix(string[] strs)
        {
            var longestPrefix = new StringBuilder();
            var counter = 0;
            while (true)
            {
                var currentChar = strs[0][counter];
                int i = 1;
                for (; i < strs.Length; i++)
                {
                    if (strs[i][counter] != currentChar)
                    {
                        break;
                    }
                }
                if (i == strs.Length)
                {
                    longestPrefix.Append(currentChar);
                    counter++;
                }
                else
                {
                    break;
                }
            }
            return longestPrefix.ToString();
        }

        public static string LongestPalindrome(string s)
        {
            var n = s.Length;
            int[] hashTable = new int[128];
            System.Array.Fill(hashTable, 0);
            var isPalindrome = true;
            for (int i = 0; i < n; i++)
            {
                hashTable[(int)s[i]] = i;
            }
            var start = 0;
            var end = 0;

            for (int i = 0; i < n; i++)
            {
                if (hashTable[(int)s[i]] > 0)
                {
                    var len = hashTable[(int)s[i]] - i;
                    isPalindrome = true;
                    for (int j = 0; j < len; j++)
                    {
                        if (s[i + j] != s[hashTable[(int)s[i]] - j])
                        {
                            isPalindrome = false;
                            break;
                        }
                    }
                    if (isPalindrome && len > end - start)
                    {
                        start = i;
                        end = hashTable[(int)s[i]];
                    }
                }
            }
            if (isPalindrome)
            {
                return s.Substring(start, end + 1 - start);
            }
            else
            {
                return "not possible";
            }
        }

        public static int[] Occurence(int input1, int[] input2)
        {
            var result = new List<int>();
            var visit = new Dictionary<int, bool>();
            for (int i = 0; i < input1; i++)
            {
                if (visit.ContainsKey(input2[i]))
                {
                    result.Add(input2[i]);
                }
                else
                {
                    visit[input2[i]] = true;
                }
            }
            if (result.Count > 0)
            {
                result.Sort();
            }
            else
            {
                result.Add(-1);
            }

            return result.ToArray();
        }

