        }

        public static int MyAtoi(string s)
        {
            var result = 0;
            var negative = false;
            int i = 0;
            while (i < s.Length && s[i] == ' ')
            {
                i++;
            }

            if (i < s.Length && s[i] == '+')
            {
                negative = false;
                i++;
            }
            else if (i < s.Length && s[i] == '-')
            {
                negative = true;
                i++;
            }

            while (i < s.Length && char.IsDigit(s[i]))
            {
                if (result > int.MaxValue / 10 ||
                    (result == int.MaxValue / 10 &&
                     s[i] - '0' > int.MaxValue % 10))
                {
                    return negative ? int.MinValue : int.MaxValue;
                }

                result = result * 10 + (s[i++] - '0');
            }

            return negative ? result * -1 : result;
        }

        public static string LongestCommonPrefix(string[] strs)
        {
            var longestPrefix = new StringBuilder();
            var counter = 0;
            while (true)
            {
                var currentChar = strs[0][counter];
                int i = 1;
                for (; i < strs.Length; i++)
                {
                    if (strs[i][counter] != currentChar)
                    {
                        break;
                    }
                }
                if (i == strs.Length)
                {
                    longestPrefix.Append(currentChar);
                    counter++;
                }
                else
                {
                    break;
                }
            }
            return longestPrefix.ToString();
        }

        public static List<List<int>> ThreeSum(int[] nums)
        {
            Array.Sort(nums);
            List<List<int>> result = new List<List<int>>();

            for (int i = 0; i < nums.Length - 2; i++)
            {
                if (i == 0 || (nums[i] != nums[i - 1]))
                {
                    var low = i + 1;
                    var high = nums.Length - 1;
                    var sum = 0-nums[i];
                    while (low < high)
                    {
                        if (nums[low] + nums[high] == sum)
                        {
                            result.Add(new List<int> { nums[i], nums[low], nums[high] });
                            while (low < high && nums[low] == nums[low + 1])
                            {
                                low++;
                            }
                            while (low < high && nums[high] == nums[high - 1])
                            {
                                high--;
                            }
                            low++;
                            high--;
                        }
                        else if (nums[low] + nums[high] > sum)
                        {
                            high--;
                        }
                        else
                        {
                            low++;
                        }
                    }
                }
            }

            return result;
        }

        public static int ThreeSumClosest(int[] nums, int target)
        {
            Array.Sort(nums);
            var diff = int.MaxValue;
            var sum = int.MaxValue;
            for (int i = 0; i < nums.Length - 2; i++)
            {
                var low = i + 1;
                var high = nums.Length - 1;
                while (low < high)
                {
                    sum = nums[i] + nums[low] + nums[high];
                    if (Math.Abs(sum - target) < Math.Abs(diff))
                    {
                        diff = target - sum;
                    }
                    if (sum < target)
                    {
                        low++;
                    }
                    else
                    {
                        high--;
                    }
                }
            }

            return target - diff;
        }

        public static string LongestPalindrome(string s)
        {
            var n = s.Length;
            int[] hashTable = new int[128];
            System.Array.Fill(hashTable, 0);
            var isPalindrome = true;
            for (int i = 0; i < n; i++)
            {
                hashTable[(int)s[i]] = i;
            }
            var start = 0;
            var end = 0;

            for (int i = 0; i < n; i++)
            {
                if (hashTable[(int)s[i]] > 0)
                {
                    var len = hashTable[(int)s[i]] - i;
                    isPalindrome = true;
                    for (int j = 0; j < len; j++)
                    {
                        if (s[i + j] != s[hashTable[(int)s[i]] - j])
                        {
                            isPalindrome = false;
                            break;
                        }
                    }
                    if (isPalindrome && len > end - start)
                    {
                        start = i;
                        end = hashTable[(int)s[i]];
                    }
                }
            }
            if (isPalindrome)
            {
                return s.Substring(start, end + 1 - start);
            }
            else
            {
                return "not possible";
            }
        }

        public static int[] Occurence(int input1, int[] input2)
        {
            var result = new List<int>();
            var visit = new Dictionary<int, bool>();
            for (int i = 0; i < input1; i++)
            {
                if (visit.ContainsKey(input2[i]))
                {
                    result.Add(input2[i]);
                }
                else
                {
                    visit[input2[i]] = true;
                }
            }
            if (result.Count > 0)
            {
                result.Sort();
            }
            else
            {
                result.Add(-1);
            }

            return result.ToArray();
        }

